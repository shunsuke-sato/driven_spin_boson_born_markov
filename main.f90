module global_variables
  implicit none
! math parameters
  real(8),parameter :: pi = 4d0*atan(1d0)
  complex(8),parameter :: zi = (0d0, 1d0)

! quantum system
  complex(8) :: zrho_dm(2,2), zU_prop(2,2)
  complex(8),allocatable :: zrho_dm_memory(:,:,:),zu_prop(:,:,:)
  
! time propagation
  integer :: nt, nt_memory
  real(8) :: dt, Tprop

! field parameters
  real(8) :: E0, omega0

  
end module global_variables
!--------------------------------------------------------------------------------------
program main
  implicit none
  use global_variables


  call input  
  call initialize
  
end program main
!--------------------------------------------------------------------------------------
subroutine input
  implicit none
  use global_variables


  Tprop = 200d0
  dt = 0.1d0
  nt = aint(Tprop/dt) + 1
  
  E0 = 0.1d0
  omega0 = 1d0
  
  
end subroutine input
!--------------------------------------------------------------------------------------
subroutine initialization
  implicit none
  use global_variables

  
end subroutine initialization
